package entities.ptis;

public class Amenities {

    private Boolean lift;
    private Boolean toilets;
    private Boolean waterTap;
    private Boolean electricity;
    private Boolean attachedBathroom;
    private Boolean waterHarvesting;
    private Boolean cableConnection;

    public Boolean getLift() {
        return lift;
    }

    public void setLift(Boolean lift) {
        this.lift = lift;
    }

    public Boolean getToilets() {
        return toilets;
    }

    public void setToilets(Boolean toilets) {
        this.toilets = toilets;
    }

    public Boolean getWaterTap() {
        return waterTap;
    }

    public void setWaterTap(Boolean waterTap) {
        this.waterTap = waterTap;
    }

    public Boolean getElectricity() {
        return electricity;
    }

    public void setElectricity(Boolean electricity) {
        this.electricity = electricity;
    }

    public Boolean getAttachedBathroom() {
        return attachedBathroom;
    }

    public void setAttachedBathroom(Boolean attachedBathroom) {
        this.attachedBathroom = attachedBathroom;
    }

    public Boolean getWaterHarvesting() {
        return waterHarvesting;
    }

    public void setWaterHarvesting(Boolean waterHarvesting) {
        this.waterHarvesting = waterHarvesting;
    }

    public Boolean getCableConnection() {
        return cableConnection;
    }

    public void setCableConnection(Boolean cableConnection) {
        this.cableConnection = cableConnection;
    }
}
